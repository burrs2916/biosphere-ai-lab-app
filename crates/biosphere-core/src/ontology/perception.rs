//! 感知抽象层
//!
//! 这个模块定义了 [`Perception`] trait，它描述了存在"可被区分"的抽象能力。
//!
//! # 哲学背景
//!
//! [`Perception`] 不是"看见"或"观察"，而是"可被区分"的抽象能力。
//! 它回答了这样一个问题：
//! > 这个存在，在什么意义上是"可被区分的"？
//!
//! 这个定义不依赖于：
//! - 具体的感知器官（眼睛、耳朵等）
//! - 具体的感知介质（光、声、电等）
//! - 具体的感知技术（UI、可视化等）
//! - 时间性或事件性（不涉及"变化"或"事件"）
//!
//! 它仅仅定义了"可被区分"的抽象条件。
//!
//! # 核心概念
//!
//! [`Perception`] 通过关联类型和方法定义了可区分性的基本维度：
//!
//! - **信号**：暴露可被检测的信息载体
//! - **区分能力**：判定两个信号是否相同的能力
//!
//! # 与 ExistenceCore 的关系
//!
//! [`ExistenceCore`] 定义了"什么是生命"
//! [`Perception`] 定义了"什么是可被区分的"
//!
//! 生命不一定需要被区分，但如果要被区分，
//! 必须满足 Perception 定义的条件。
//!
//! # 设计原则
//!
//! - **抽象性**：不绑定任何具体感知技术
//! - **最小性**：只包含绝对必要的区分条件
//! - **稳定性**：一旦定义，保持稳定
//! - **普适性**：适用于任何可能的区分方式
//! - **静态性**：不涉及时间、事件或变化
//!
//! # 核心层 vs 应用层
//!
//! ## 核心层（这里）
//! - 信号：静态的信息载体
//! - 区分：判定信号是否相同
//! - 不涉及：时间、事件、叙述、语义
//!
//! ## 应用层（UI、AI、Simulation）
//! - 事件：对变化的叙述
//! - 时间化：引入时间维度
//! - 语义化：添加意义和解释
//!
//! # 作者与时间
//!
//! 作者：BURRS  
//! 创建时间：2026年1月3日
//!
//! 这是一个永恒的区分定义，不随时间而改变。

use std::any::Any;

/// 感知抽象的核心 trait
///
/// [`Perception`] 定义了存在"可被区分"的抽象能力。
/// 它不是"看见"，而是"区分"。
///
/// # 设计原则
///
/// - **区分性**：感知的本质是区分，不是呈现
/// - **信号性**：通过信号暴露信息，不暴露内部结构
/// - **静态性**：不涉及时间、事件或变化
/// - **最小性**：只包含绝对必要的区分条件
///
/// # 与生物学的关系
///
/// 在生物学中，细胞通过以下方式"可被区分"：
/// - 边界信号：膜电位、膜蛋白（区分内外）
/// - 状态信号：代谢产物、离子浓度（区分状态）
///
/// 这些都不是"眼睛"或"UI"，而是"可被区分的信号"。
///
/// # 实现注意
///
/// 实现者应该将 Perception 视为一个"信号接口"，它的职责是：
/// 1. 暴露信号（可被检测的信息）
/// 2. 提供区分能力（判定信号是否相同）
///
/// Perception 不应该：
/// - 定义具体的感知方式（视觉、听觉等）
/// - 定义具体的呈现技术（UI、Canvas 等）
/// - 引入时间、事件或变化的概念
/// - 承诺可观测性或可理解性
/// - 提供叙述或语义解释
pub trait Perception: Any {
    /// 信号类型
    ///
    /// 信号是可被检测的信息载体。
    /// 它不暴露内部结构，只暴露"信号存在"这一事实。
    ///
    /// # 设计约束
    ///
    /// 信号类型应该是：
    /// - 静态的（不涉及时间）
    /// - 结构化的（可以被比较）
    /// - 最小化的（只包含必要信息）
    ///
    /// 信号类型不应该：
    /// - 包含时间戳或时间信息
    /// - 包含事件或变化信息
    /// - 包含语义或解释信息
    type Signal;

    /// 获取信号
    ///
    /// 返回当前的可检测信号。
    ///
    /// # 保证
    ///
    /// 信号只表达"当前状态"这一事实，
    /// 不涉及时间、事件或变化。
    ///
    /// # 注意
    ///
    /// 这个方法是静态的，不涉及时间或事件。
    /// 它返回的是"当前的信号"，而不是"信号的变化"。
    fn signal(&self) -> Self::Signal;

    /// 区分信号
    ///
    /// 判定两个信号是否相同。
    ///
    /// # 参数
    ///
    /// - `a`：第一个信号
    /// - `b`：第二个信号
    ///
    /// # 返回
    ///
    /// - `true`：两个信号相同
    /// - `false`：两个信号不同
    ///
    /// # 保证
    ///
    /// 这个方法提供区分能力，不提供叙述或解释。
    /// 它不说明"为什么相同"或"为什么不同"，
    /// 只回答"是否相同"。
    fn distinguish(&self, a: &Self::Signal, b: &Self::Signal) -> bool;
}

/// 辅助函数：将 trait 对象转换为 Any
///
/// 允许在运行时检查具体类型。
pub fn as_any<T: Perception>(perception: &T) -> &dyn Any {
    perception
}
