//! 生命系统的最原始定义
//!
//! 这个模块包含了 [`ExistenceCore`] trait 的纯粹定义，它定义了生命型系统存在所需的最小必要条件。
//!
//! # 哲学背景
//!
//! [`ExistenceCore`] 不是一个实现，而是一组公理化的约束条件。它回答了这样一个问题：
//! > 在任何系统中，什么是"生命得以出现与持续存在"的最小必要条件？
//!
//! 这个定义不依赖于：
//! - 具体的生物形态
//! - 物质基础
//! - 计算载体
//! - 功能需求
//!
//! 它仅仅定义了生命存在的可能性空间。
//!
//! # 五个基本维度
//!
//! [`ExistenceCore`] 通过五个关联类型定义了生命系统的基本结构：
//!
//! - [`ExistenceCore::Boundary`] - 区分"我"与"非我"的机制
//! - [`ExistenceCore::State`] - 内部状态的载体
//! - [`ExistenceCore::Drive`] - 维持非平衡状态的驱动力
//! - [`ExistenceCore::Rules`] - 状态转换的内在规则
//! - [`ExistenceCore::Propagation`] - 在时间中延续自身的能力
//!
//! # 不可修改性
//!
//! **重要**：这个定义被视为数学公理一样的存在，一旦确定就永不改变。
//! 任何对生命概念的扩展都应该在更高的层次进行，而不是修改这个核心定义。
//!
//! # 作者与时间
//!
//! 作者：BURRS  
//! 创建时间：2025年12月20日  
//!  
//! 这是一个永恒定义，不随时间而改变。

/// 生命型系统的核心 trait。
///
/// [`ExistenceCore`] 定义了生命得以存在的五个最小必要条件。任何实现了这个 trait 的系统
/// 都具备了成为生命型系统的基本潜质。
///
/// # 设计原则
///
/// - **最小性**：只包含绝对必要的条件
/// - **抽象性**：不绑定任何具体实现细节
/// - **稳定性**：一旦定义，永不修改
/// - **普适性**：适用于任何可能的系统载体
///
/// # 与生物学的关系
///
/// 这个定义不是对生物生命的模拟，而是对生命本质的抽象。它捕捉的是：
/// - 什么使得一个系统能够"存在"而非"发生"
/// - 什么使得一个系统能够持续而非瞬态
/// - 什么使得一个系统能够演化而非静止
///
/// # 实现注意
///
/// 实现者应该将这五个关联类型视为生命系统的"基本维度"，
/// 而不是具体的功能模块。每个类型都代表了一个深层的哲学概念。
pub trait ExistenceCore {
    /// 边界机制 - 区分"我"与"非我"的能力。
    ///
    /// 没有边界，就没有"系统自身"的概念。边界定义了：
    /// - 什么是内部状态，什么是外部环境
    /// - 什么属于系统，什么不属于系统
    /// - 系统的身份和独立性
    ///
    /// 在物理生命中，这对应于细胞膜等结构。
    /// 在软件生命中，这可能是访问控制、封装机制等。
    type Boundary;

    /// 状态载体 - 系统内部可积累、可变化的信息。
    ///
    /// 状态使得系统能够：
    /// - 拥有历史而非仅仅是瞬态
    /// - 积累经验和记忆
    /// - 作为演化的基础
    ///
    /// 状态必须能够持续存在，而不仅仅是临时变量。
    type State;

    /// 驱动力 - 维持系统远离平衡状态的力量。
    ///
    /// 生命系统必须持续消耗资源来维持自身结构。
    /// 这种非平衡状态是：
    /// - 生命与非生命的根本区别
    /// - 系统持续活动的源泉
    /// - 复杂性行为涌现的基础
    ///
    /// 驱动力不是能量本身，而是能量流动的组织方式。
    type Drive;

    /// 规则系统 - 状态转换的内在逻辑。
    ///
    /// 规则使得系统能够：
    /// - 自洽地运行而不依赖外部干预
    /// - 对环境变化做出可预测的反应
    /// - 维持内部结构的稳定性
    ///
    /// 这些规则是内在的，不是外部强加的。
    type Rules;

    /// 延续能力 - 系统在时间中维持自身的能力。
    ///
    /// 延续不仅仅是存在，还包括：
    /// - 复制或再生产的能力
    /// - 允许变异但保持本质
    /// - 为演化提供可能性
    ///
    /// 这是生命能够跨越时间维度的基础。
    type Propagation;
}