//! 场域的基础定义
//!
//! 这个模块定义了 [`Field`] trait 的纯粹抽象，它描述了存在得以共处同一个现实尺度的最小结构。
//!
//! # 哲学背景
//!
//! [`Field`] 不是控制者，不是调度器，不是引擎，而只是：存在的场域。
//! 它回答了这样一个问题：
//! > 哪些存在，被认为处在同一个现实中？
//!
//! 这个定义体现了以下核心原则：
//! - **共处性**：定义“哪些存在被承认为共处”
//! - **被动性**：不产生行为，只使行为成为可能
//! - **存在论**：只定义“关系成立”，不解释“为什么”
//! - **尺度性**：允许嵌套，上层不控制下层
//!
//! # 核心概念
//!
//! [`Field`] 通过两个关联类型定义了场域的基本结构：
//!
//! - [`Field::Environment`] - 场域所承认的环境
//! - [`Field::Embodiment`] - 场域所承认的具身
//!
//! # 与生命的关系
//!
//! 场域与生命的关系是：
//! - 场域承认具身的共处，但不“拥有”具身
//! - 具身可以被多个场域承认，具身对此无感知
//! - 迁移不是行为，而是“存在承认关系的改变”
//!
//! # 不可修改性
//!
//! **重要**：这个场域定义一旦确定就保持稳定，任何对共处概念的扩展都应该在更高层次进行。
//!
//! # 作者与时间
//!
//! 作者：BURRS  
//! 创建时间：2025年12月21日  
//!
//! 这是一个永恒的场域定义，不随时间而改变。

use crate::ontology::environment::Environment;
use crate::ontology::embodiment::Embodiment;

/// 场域的核心 trait。
///
/// [`Field`] 定义了存在得以共处同一个现实尺度的最小必要条件。
/// 它是“共处得以成立的最小结构”。
///
/// # 设计原则
///
/// - **共处性**：只定义“哪些存在被承认为共处”
/// - **被动性**：不产生行为，只使行为成为可能
/// - **存在论**：只定义关系成立，不解释因果
/// - **嵌套性**：允许尺度嵌套，但上层不控制下层
///
/// # 与科学的关系
///
/// 这个定义不是对物理场的模拟，而是对“共处可能性”的抽象。它捕捉的是：
/// - 什么使得多个存在可以“在同一个现实中”
/// - 什么使得交互、影响、演化成为可能
/// - 什么使得尺度、层级、嵌套得以成立
///
/// # 实现注意
///
/// 实现者应该将场域视为一个“存在论前提”，它的职责是：
/// 1. 承认某些环境和具身的共处
/// 2. 提供这种共处关系的结构框架
/// 3. 允许嵌套但不引入控制或因果
///
/// 场域不应该：
/// - 管理或调度具身
/// - 控制或推进环境
/// - 引入时间机制或因果链
/// - 承诺公平性、一致性或完整性
pub trait Field {
    /// 场域所承认的环境类型。
    ///
    /// 环境在场域中被承认其存在，但场域不控制环境的推进或演化。
    /// 环境的状态变化完全由环境自身的规则决定。
    type Environment: Environment;

    /// 场域所承认的具身类型。
    ///
    /// 具身在场域中被承认其存在，但场域不管理具身的生命周期。
    /// 具身可以同时被多个场域承认，对此具身本身无感知。
    type Embodiment: Embodiment;
}