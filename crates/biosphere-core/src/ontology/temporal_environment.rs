//! 时间性环境的基础定义
//!
//! 这个模块定义了 [`TemporalEnvironment`] trait，它描述了承认时间维度存在的世界形态。
//!
//! # 哲学背景
//!
//! [`TemporalEnvironment`] 不是一个主动的时间机制，而是被动承认世界状态演化具有不可逆先后关系的环境。
//! 它回答了这样一个问题：
//! > 世界是否承认“过去”与“现在”的区别？
//!
//! 这个定义体现了以下核心原则：
//! - **序性**：世界状态的变化具有不可逆的先后关系
//! - **被动性**：时间不是机制，而是世界演化的涌现秩序
//! - **最小性**：只提供“序”的承认，不提供时间单位或调度
//!
//! # 核心概念
//!
//! [`TemporalEnvironment`] 通过 [`advance`] 方法定义了世界的基本时间属性：
//!
//! - 世界状态可以沿“序”向前推进
//! - 推进产生不可逆的先后关系
//! - 生命存在于这个序中，但不控制它
//!
//! # 与时间的关系
//!
//! 时间在本体系中的理解是：
//! - 时间不是属性，不是机制，不是调度器
//! - 时间是世界状态演化中涌现的不可逆秩序
//! - [`TemporalEnvironment`] 只是承认这种秩序的存在
//!
//! # 不可修改性
//!
//! **重要**：这个时间性环境定义一旦确定就保持稳定，任何对时间概念的扩展都应该在更高层次进行。
//!
//! # 作者与时间
//!
//! 作者：BURRS  
//! 创建时间：2025年12月21日  
//!
//! 这是一个永恒的时间性环境定义，不随时间而改变。

use crate::ontology::environment::Environment;

/// 时间性环境的核心trait
///
/// [`TemporalEnvironment`] 定义了承认世界状态演化具有时间序的环境。
/// 它描述了世界如何作为被动承载者，为生命提供存在的时间维度。
///
/// # 设计原则
///
/// - **序性**：环境承认状态变化具有不可逆的先后关系
/// - **被动性**：环境不驱动时间，只提供序的框架
/// - **最小性**：只定义“向前”这一最小时间操作
///
/// # 实现注意
///
/// 实现者应该将时间性环境视为一个“序场”，它的职责是：
/// 1. 承认世界状态演化具有先后关系
/// 2. 提供将世界向前推进的最小操作
/// 3. 不隐含物理时间、时钟或调度概念
///
/// 时间性环境不应该：
/// - 定义时间单位或物理时间
/// - 保证公平性或一致性
/// - 控制或调用生命
/// - 暗示确定性或随机性
pub trait TemporalEnvironment: Environment {
    /// 承认世界状态之间的不可逆先后关系发生了一次前移
    ///
    /// 这个方法承认世界状态沿时间序发生了不可逆的前移。
    /// 它不隐含物理时间、时钟或调度，只表达"顺序向前"这一最小概念。
    ///
    /// # 哲学含义
    ///
    /// 这不是"推进"或"驱动"时间，而是"承认"世界状态之间的不可逆先后关系。
    /// 时间是世界演化的涌现秩序，不是机制。TemporalEnvironment 只是被动地确认这种秩序的存在。
    fn advance(&mut self);
}