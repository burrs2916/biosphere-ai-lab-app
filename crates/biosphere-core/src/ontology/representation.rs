//! 表征抽象层
//!
//! 这个模块定义了 [`Representation`] trait，它描述了存在"可被呈现"的抽象结构。
//!
//! # 哲学背景
//!
//! [`Representation`] 不是"UI"、"视图"或"可视化"，而是"可被映射的结构"。
//! 它回答了这样一个问题：
//! > 这个存在，在什么意义上是"可被呈现的"？
//!
//! 这个定义不依赖于：
//! - 具体的呈现方式（视觉、听觉、触觉等）
//! - 具体的呈现技术（Canvas、DOM、WebGL 等）
//! - 具体的呈现介质（屏幕、纸张、空气等）
//! - 具体的解释或语义（标签、名称、描述等）
//!
//! 它仅仅定义了"可被呈现"的抽象结构。
//!
//! # 核心概念
//!
//! [`Representation`] 通过关联类型和方法定义了可呈现性的基本维度：
//!
//! - **维度**：表征的空间或抽象维度
//! - **拓扑**：表征的结构关系
//! - **数据**：表征的原始信息
//!
//! # 与 Perception 的关系
//!
//! [`Perception`] 定义了"什么是可被区分的"
//! [`Representation`] 定义了"什么是可被呈现的"
//!
//! 感知是前提，呈现是结果。
//! 感知提供信号，呈现提供结构。
//!
//! # 设计原则
//!
//! - **抽象性**：不绑定任何具体呈现技术
//! - **结构性**：强调结构关系，而非具体呈现
//! - **可映射性**：可以被映射到任何呈现方式
//! - **最小性**：只包含绝对必要的呈现条件
//! - **非解释性**：不承诺可理解性或可解释性
//!
//! # 核心层 vs 应用层
//!
//! ## 核心层（这里）
//! - 结构：维度、拓扑、数据
//! - 可映射性：可以被映射到任何呈现方式
//! - 不承诺：可理解性、可解释性、语义化
//!
//! ## 应用层（UI、AI、Tooling）
//! - 解释性：如何解释、给谁解释、是否解释
//! - 语义化：添加标签、名称、描述
//! - 可理解性：提供说明、文档、帮助
//!
//! # 防腐公理
//!
//! **Representation 只保证"可被映射"，不保证"可被理解"**
//!
//! 一旦 core 里出现这些东西，就已经错了：
//! - label
//! - name
//! - description
//! - explain()
//! - semantic_hint
//! - metadata（只要是"给人看的"）
//!
//! 正确的态度是：
//!
//! Representation 提供的是：
//! - 结构
//! - 维度
//! - 可投影性
//!
//! UI / AI / Tooling 决定：
//! - 怎么解释
//! - 给谁解释
//! - 是否解释
//!
//! # 作者与时间
//!
//! 作者：BURRS  
//! 创建时间：2026年1月3日
//!
//! 这是一个永恒的表征定义，不随时间而改变。

/// 表征抽象的核心 trait
///
/// [`Representation`] 定义了存在"可被呈现"的抽象结构。
/// 它不是"UI"，而是"可被映射的结构"。
///
/// # 设计原则
///
/// - **结构性**：表征是结构，不是呈现
/// - **可映射性**：可以被映射到任何呈现方式
/// - **独立性**：不依赖具体的呈现技术
/// - **最小性**：只包含绝对必要的呈现条件
/// - **非解释性**：不承诺可理解性或可解释性
///
/// # 与 UI 的关系
///
/// 在应用层，表征可以被映射到：
/// - 视觉：Canvas、SVG、DOM
/// - 听觉：音频、语音
/// - 数值：图表、仪表盘
/// - 文本：日志、报告
///
/// 但在核心层，表征不承诺任何具体的呈现方式。
///
/// # 防腐公理
///
/// **Representation 只保证"可被映射"，不保证"可被理解"**
///
/// 一旦 core 里出现这些东西，就已经错了：
/// - label
/// - name
/// - description
/// - explain()
/// - semantic_hint
/// - metadata（只要是"给人看的"）
///
/// 正确的态度是：
///
/// Representation 提供的是：
/// - 结构
/// - 维度
/// - 可投影性
///
/// UI / AI / Tooling 决定：
/// - 怎么解释
/// - 给谁解释
/// - 是否解释
///
/// # 实现注意
///
/// 实现者应该将 Representation 视为一个"结构接口"，它的职责是：
/// 1. 定义表征的维度（空间或抽象）
/// 2. 定义表征的拓扑（结构关系）
/// 3. 提供表征的数据（原始信息）
///
/// Representation 不应该：
/// - 定义具体的呈现方式（视觉、听觉等）
/// - 定义具体的呈现技术（Canvas、DOM 等）
/// - 承诺可理解性或可解释性
/// - 添加标签、名称、描述等语义化信息
pub trait Representation {
    /// 维度类型
    ///
    /// 维度定义了表征的空间或抽象维度。
    /// 它可以是：
    /// - 空间维度：2D、3D 等
    /// - 抽象维度：时间、状态、关系等
    type Dimension;

    /// 拓扑类型
    ///
    /// 拓扑定义了表征的结构关系。
    /// 它可以是：
    /// - 空间拓扑：网格、树、图等
    /// - 抽象拓扑：序列、层次、网络等
    type Topology;

    /// 数据类型
    ///
    /// 数据定义了表征的原始信息。
    /// 它可以是：
    /// - 数值数据：标量、向量、矩阵等
    /// - 符号数据：文本、标签、标识符等
    /// - 结构数据：记录、对象、图等
    type Data;

    /// 获取维度
    ///
    /// 返回表征的维度集合。
    ///
    /// # 保证
    ///
    /// 维度只表达"空间或抽象维度"这一事实，
    /// 不承诺具体的坐标系或度量单位。
    ///
    /// # 设计说明
    ///
    /// 维度不是坐标空间，而是"表征所处的抽象维度集合"。
    /// 一个表征可以同时存在于多个维度空间中（例如：空间维度 + 时间维度 + 状态维度）。
    fn dimensions(&self) -> &[Self::Dimension];

    /// 获取拓扑
    ///
    /// 返回表征的拓扑结构。
    ///
    /// # 保证
    ///
    /// 拓扑只表达"结构关系"这一事实，
    /// 不承诺具体的布局算法或渲染方式。
    fn topology(&self) -> Self::Topology;

    /// 获取数据
    ///
    /// 返回表征的原始数据。
    ///
    /// # 保证
    ///
    /// 数据只表达"原始信息"这一事实，
    /// 不承诺具体的编码格式或传输协议。
    fn data(&self) -> Self::Data;
}

/// 表征拓扑的枚举
///
/// 定义了几种常见的表征拓扑类型。
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum RepresentationTopology {
    /// 线性拓扑（序列）
    Linear,

    /// 层次拓扑（树）
    Hierarchical,

    /// 网格拓扑（矩阵）
    Grid,

    /// 图拓扑（网络）
    Graph,

    /// 球形拓扑（球面）
    Spherical,

    /// 自定义拓扑
    Custom,
}

/// 表征维度的枚举
///
/// 定义了几种常见的表征维度类型。
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum RepresentationDimension {
    /// 一维
    OneD,

    /// 二维
    TwoD,

    /// 三维
    ThreeD,

    /// 时间维度
    Temporal,

    /// 状态维度
    State,

    /// 关系维度
    Relational,

    /// 自定义维度
    Custom,
}
