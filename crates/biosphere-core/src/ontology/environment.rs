//! 环境系统的基础定义
//!
//! 这个模块定义了 [`Environment`] trait 的纯粹抽象，它描述了世界作为生命存在的外部条件。
//!
//! # 哲学背景
//!
//! [`Environment`] 不是一个主动的行动者，而是被动承载生命发生的场所。它回答了这样一个问题：
//! > 生命得以存在和演化的外部条件是什么？
//!
//! 这个定义体现了以下核心原则：
//! - **被动性**：世界不主动干预生命，只提供存在的条件
//! - **条件性**：生命只能感知条件，不能直接访问世界的内部状态
//! - **演化性**：世界自身状态可以推进，从而改变暴露给生命的条件
//!
//! # 核心概念
//!
//! [`Environment`] 通过两个关联类型定义了世界的基本结构：
//!
//! - [`Environment::State`] - 世界自身的演化状态
//! - [`Environment::Conditions`] - 暴露给生命的当前条件
//!
//! # 与生命的关系
//!
//! 环境与生命的关系是：
//! - 世界提供条件，生命感知条件
//! - 世界推进自身状态，生命对此做出响应
//! - 世界不调用生命，生命主动适应世界
//!
//! # 不可修改性
//!
//! **重要**：这个环境定义一旦确定就保持稳定，任何对世界-生命交互的扩展都应该在更高层次进行。
//!
//! # 作者与时间
//!
//! 作者：BURRS  
//! 创建时间：2025年12月20日  
//! 
//! 这是一个永恒的环境定义，不随时间而改变。

use crate::ontology::conditions::Conditions;

/// 世界状态的标记trait
/// 
/// [`WorldState`] 标记了世界自身状态的类型。
/// 世界状态是环境内部的演化载体，生命无法直接访问。
pub trait WorldState {}

/// 环境型系统的核心trait
/// 
/// [`Environment`] 定义了生命得以存在和演化的外部条件。
/// 它描述了世界如何作为被动承载者，为生命提供存在的场域。
///
/// # 设计原则
/// 
/// - **被动性**：环境不主动行动，只提供条件
/// - **条件性**：生命只能感知条件，不能感知整个世界
/// - **演化性**：世界状态可以推进，条件随之变化
///
/// # 实现注意
/// 
/// 实现者应该将环境视为一个“条件场”，它的职责是：
/// 1. 持有并管理世界自身的演化状态
/// 2. 根据当前状态生成暴露给生命的条件
/// 3. 允许时间推进来改变状态和条件
///
/// 环境不应该：
/// - 主动调用或干预生命
/// - 理解或解释生命的行为
/// - 为生命设定目标或方向
pub trait Environment {
    /// 世界自身的状态类型
    /// 
    /// 状态代表了世界内部的完整演化情况，是条件生成的基础。
    /// 生命无法直接访问这个状态，只能通过条件间接感知。
    type State: WorldState;

    /// 暴露给生命的条件类型
    /// 
    /// 条件是世界状态的局部表现，生命通过感知条件来理解外部世界。
    /// 条件应该是只读的、瞬时的、局部的世界片段。
    type Conditions: Conditions;

    /// 承认世界状态的更新
    ///
    /// 这个方法承认世界状态在当前序位下发生了变化。
    /// 状态的更新会间接改变暴露给生命的条件。
    ///
    /// # 哲学含义
    ///
    /// 这不是"推进"或"驱动"世界演化，而是"承认"世界状态的变化。
    /// 世界状态的变化是由外部因素（如迁移、主体选择等）引起的，
    /// Environment 只是被动地确认这种变化。
    fn step(&mut self);

    /// 获取当前暴露给生命的条件
    /// 
    /// 返回的条件应该反映世界当前状态的可感知片段。
    /// 同一个世界状态下，条件应该保持一致性。
    fn conditions(&self) -> &Self::Conditions;
}