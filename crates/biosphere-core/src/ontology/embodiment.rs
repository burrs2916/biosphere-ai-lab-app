//! 生命具身的基础定义
//!
//! 这个模块定义了 [`Embodiment`] trait 的纯粹抽象，它描述了生命得以在世界中具体存在的方式。
//!
//! # 哲学背景
//!
//! [`Embodiment`] 不是生命本身，而是生命得以暴露在世界条件与时间序中的存在方式。
//! 它回答了这样一个问题：
//! > 生命如何"站"在这个世界里？
//!
//! 这个定义连接了：
//! - [`ExistenceCore`] - 定义"什么可以被称为生命"
//! - [`Perception`] - 定义"什么是可被区分的"
//! - [`Representation`] - 定义"什么是可被映射的"
//!
//! # 核心概念
//!
//! [`Embodiment`] 是"唯一跨层对象"，它是：
//! - 现象学中的"给予方式"
//! - 工程中的"稳定接口面"
//! - 架构中的"隔离层"
//! - 存在被世界承认时，唯一合法的形态
//!
//! # 架构公理
//!
//! **任何应用层代码，只要直接依赖 ExistenceCore，都是架构违规**
//!
//! Embodiment 是应用层与核心层之间的唯一合法接口。
//!
//! # 与世界的关系
//!
//! 具身与世界的关系是：
//! - 世界通过 Embodiment 访问生命
//! - 世界不直接访问 ExistenceCore、Perception 或 Representation
//! - Embodiment 提供对核心抽象的稳定访问接口
//!
//! # 不可修改性
//!
//! **重要**：这个具身定义一旦确定就保持稳定，任何对生命-世界交互的扩展都应该在更高层次进行。
//!
//! # 作者与时间
//!
//! 作者：BURRS  
//! 创建时间：2025年12月21日  
//!
//! 这是一个永恒的具身定义，不随时间而改变。

use crate::ontology::existence_core::ExistenceCore;
use crate::ontology::perception::Perception;
use crate::ontology::representation::Representation;

/// 生命具身的核心 trait。
///
/// [`Embodiment`] 定义了生命得以在世界中具体存在的方式。
/// 它是生命与世界之间的唯一接触点，也是应用层与核心层之间的唯一合法接口。
///
/// # 设计原则
///
/// - **接触性**：是生命与世界的唯一接口
/// - **隔离性**：世界不直接访问核心抽象，只通过 Embodiment 访问
/// - **稳定性**：提供对核心抽象的稳定访问接口
/// - **最小性**：只包含绝对必要的访问方法
///
/// # 架构公理
///
/// **任何应用层代码，只要直接依赖 ExistenceCore，都是架构违规**
///
/// Embodiment 是应用层与核心层之间的唯一合法接口。
///
/// # 与生命的关系
///
/// 这个定义不是对生命的控制，而是对生命存在方式的抽象。它捕捉的是：
/// - 生命如何暴露其存在核心
/// - 生命如何暴露其可区分性
/// - 生命如何暴露其可映射性
///
/// # 实现注意
///
/// 实现者应该将具身视为一个"稳定接口面"，它的职责是：
/// 1. 持有内在的生命定义
/// 2. 提供对核心抽象的访问接口
/// 3. 隔离核心层和应用层
///
/// 具身不应该：
/// - 定义生命规则或行为
/// - 控制世界状态或推进
/// - 引入时间机制或调度
/// - 承诺存活、公平或意义
/// - 直接暴露核心抽象的内部实现
pub trait Embodiment {
    /// 存在核心类型
    ///
    /// 定义了这个具身所持有的存在核心的具体类型。
    type Existence: ExistenceCore;

    /// 感知类型
    ///
    /// 定义了这个具身所持有的感知接口的具体类型。
    type PerceptionImpl: Perception;

    /// 表征类型
    ///
    /// 定义了这个具身所持有的表征接口的具体类型。
    type RepresentationImpl: Representation;

    /// 获取存在核心
    ///
    /// 返回生命存在核心的不可变引用。
    ///
    /// # 架构约束
    ///
    /// 这是世界访问 ExistenceCore 的唯一合法方式。
    /// 任何直接访问 ExistenceCore 的代码都是架构违规。
    ///
    /// # 返回值
    ///
    /// 返回 ExistenceCore 的不可变引用。
    fn existence(&self) -> &Self::Existence;

    /// 获取感知接口
    ///
    /// 返回生命感知接口的不可变引用。
    ///
    /// # 架构约束
    ///
    /// 这是世界访问 Perception 的唯一合法方式。
    /// 任何直接访问 Perception 的代码都是架构违规。
    ///
    /// # 返回值
    ///
    /// 返回 Perception 的不可变引用。
    fn perception(&self) -> &Self::PerceptionImpl;

    /// 获取表征接口
    ///
    /// 返回生命表征接口的不可变引用。
    ///
    /// # 架构约束
    ///
    /// 这是世界访问 Representation 的唯一合法方式。
    /// 任何直接访问 Representation 的代码都是架构违规。
    ///
    /// # 返回值
    ///
    /// 返回 Representation 的不可变引用。
    fn representation(&self) -> &Self::RepresentationImpl;
}
